#include "_app.h"


/********************************************************************
 *	DEFINIÇÕES DE HARDWARE
 ********************************************************************/
gpio_t led = {GPIOA, 5, OUTPUT};


/********************************************************************
 *	PROGRAMA PRINCIPAL
 ********************************************************************/
void _main (void)
{
	ConfiguraGPIO(&led);

	while (1)
	{
		EscreveGPIO(&led, 1);
		HAL_Delay(1000);
		EscreveGPIO(&led, 0);
		HAL_Delay(1000);
	}
}
